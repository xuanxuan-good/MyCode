# @lc code=start
class Solution:
    def lemonadeChange(self, bills: List[int]) -> bool:
        # 贪心 5 10(5) 20(5-10,555)
        five, ten = 0, 0
        for bill in bills:
            if bill == 5:
                five += 1
            elif bill == 10:
                if not five:
                    return False
                five -= 1
                ten += 1
            else:
                if five and ten:
                    five -= 1
                    ten -= 1
                elif five >= 3:
                    five -= 3
                else:
                    return False
        return True
# @lc code=end

